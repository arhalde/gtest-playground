#include "calculator.h"

int add(int x,int y){
    int result=0;
    result=x+y;
    return result;
}

int sub(int x, int y)
{
    int result = x - y;
    return result;
}

long mul(int x, int y)
{
    int result = x * y;
    return result;
}

float div(int x, int y)
{
    float result = x/y;
    return result;
}